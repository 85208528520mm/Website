 <br><h2>Реестр акционеров</h2><br>
<p> &nbsp&nbsp&nbsp&nbspАкционер – физическое или юридическое лицо, которое владеет акциями в капитале акционерного общества (АО, ОАО, ЗАО, ПАО, НАО). Акционерные общества в основном делятся на 2 вида:
	<li>Открытое (Открытое акционерное общество, Публичное акционерное общество);</li>
  <li>Закрытое (Закрытое акционерное общество, Непубличное акционерное общество).</li></p>
 <br><p>&nbsp&nbsp&nbsp&nbspАкция акционерного общества – ценная бумага, выпускаемая организацией, дающая право их держателям (Акционерам) на управление компанией, на получение прибыли. Высшим органом управления акционерного общества является собрание акционеров. Сведения об Акционерах компании хранятся в Реестре акционеров. С 2014 года право ведения Реестра акционеров общества дано только Регистратору, специализированному профессиональному участнику рынка ценных бумаг. Реестр акционеров содержит полную информацию об Акционерах, список акционеров, данные о проводимых сделках с ценными бумагами общества. Надзор за ведением Реестра акционеров производит служба Банка России по финансовым рынкам. В Едином государственном реестре юридических лиц содержится запись об учредителях акционерных обществ и о реестродержателе, ведущем актуальный Реестр акционеров.</p>
 <br><p>Участником акционерного общества (Акционером) может стать любой дееспособный гражданин Российской Федерации, достигший 18-тилетнего возраста. Акционеры не несут юридической и материальной ответственности за действия общества. Ответственность акционерного общества по своим обязательствам ограничивается рамками его имущества (ст.3, п.1 Федеральный закон "Об акционерных обществах" от 26.12.1995 №208-ФЗ) и оно не может нести ответственность за обязательства акционеров.</p>
 <br><h4>Особенности акционерных обществ</h4>
	 <li>Публичные акционерные общества (ПАО, ОАО)</li>
	   <li>Акции в свободной продаже, в т.ч. на бирже.</li>
	   <li>Обязательная публикация отчетности (финансовые результаты компании) в открытом доступе.</li>
	   <li>Обязательно наличие совета директоров акционерного общества.</li>
		<br><h4> Непубличные акционерные общества (НАО, ЗАО)</h4>
   <li>Состоят не более из 50-ти участников (при превышении 50-ти участников общество должно быть преобразовано в ПАО).</li>
   <li>Участники имеют преимущественное право покупки акций, продаваемых другими участниками.</li><br>

